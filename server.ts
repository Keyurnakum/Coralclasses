// SPA Fallback middleware - route all non-API requests to index.html
app.use((req, res, next) => {
  if (req.method === 'GET' && !req.path.startsWith('/api') && !req.path.includes('.')) {
    res.sendFile(path.join(__dirname, 'dist', 'index.html'), (err) => {
      if (err) {
        res.status(404).json({ ok: false, error: 'Not found' });
      }
    });
  } else {
    next();
  }
});

ensureDatabaseReady()
  .then(() => {
    app.listen(port, () => {
      console.log(`Supabase API server running on http://localhost:${port}`);
    });
  })
  .catch((error) => {
    console.warn('Database connection failed:', error.message);
    console.warn('Starting server in development mode without database...');
    app.listen(port, () => {
      console.log(`Development API server running on http://localhost:${port} (no database)`);
    });
  });
