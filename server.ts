import dotenv from 'dotenv';
import express from 'express';
import { Pool } from 'pg';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

dotenv.config({ path: '.env.local' });
dotenv.config();

const app = express();
const port = Number(process.env.PORT || 8787);
const databaseUrl = process.env.DATABASE_URL;

app.use(express.json());
app.use((_, res, next) => {
  res.header('Access-Control-Allow-Origin', '*');
  res.header('Access-Control-Allow-Headers', 'Origin, X-Requested-With, Content-Type, Accept');
  res.header('Access-Control-Allow-Methods', 'GET,POST,OPTIONS');
  next();
});

// Serve static files from dist folder
app.use(express.static(path.join(__dirname, 'dist')));

const pool = databaseUrl
  ? new Pool({
      connectionString: databaseUrl,
      ssl: { rejectUnauthorized: false },
    })
  : null;

async function ensureDatabaseReady() {
  if (!pool) {
    throw new Error('DATABASE_URL is missing. Add it to .env.local before starting the server.');
  }

  await pool.query(`
    CREATE TABLE IF NOT EXISTS users (
      id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      email TEXT UNIQUE NOT NULL,
      name TEXT NOT NULL,
      role TEXT NOT NULL DEFAULT 'student',
      created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
    )
  `);

  await pool.query(`
    CREATE TABLE IF NOT EXISTS courses (
      id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      name TEXT NOT NULL,
      description TEXT,
      instructor_id BIGINT REFERENCES users(id),
      subject TEXT NOT NULL,
      level TEXT,
      duration_hours INT,
      created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
    )
  `);

  await pool.query(`
    CREATE TABLE IF NOT EXISTS materials (
      id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      course_id BIGINT REFERENCES courses(id) ON DELETE CASCADE,
      title TEXT NOT NULL,
      content TEXT,
      type TEXT,
      url TEXT,
      created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
    )
  `);

  await pool.query(`
    CREATE TABLE IF NOT EXISTS tests (
      id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      course_id BIGINT REFERENCES courses(id) ON DELETE CASCADE,
      title TEXT NOT NULL,
      description TEXT,
      total_questions INT,
      duration_minutes INT,
      created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
    )
  `);

  await pool.query(`
    CREATE TABLE IF NOT EXISTS test_responses (
      id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      test_id BIGINT REFERENCES tests(id) ON DELETE CASCADE,
      user_id BIGINT REFERENCES users(id) ON DELETE CASCADE,
      score INT,
      total_marks INT,
      completed_at TIMESTAMPTZ,
      created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
    )
  `);

  await pool.query(`
    CREATE TABLE IF NOT EXISTS enrollments (
      id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      user_id BIGINT REFERENCES users(id) ON DELETE CASCADE,
      course_id BIGINT REFERENCES courses(id) ON DELETE CASCADE,
      enrolled_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
      UNIQUE(user_id, course_id)
    )
  `);

  await pool.query(`
    CREATE TABLE IF NOT EXISTS inquiries (
      id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      name TEXT NOT NULL,
      phone TEXT NOT NULL,
      course TEXT NOT NULL,
      message TEXT,
      created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
    )
  `);
}

app.get('/api/health', async (_req, res) => {
  try {
    if (!pool) {
      return res.status(503).json({ ok: false, error: 'DATABASE_URL is missing.' });
    }

    const result = await pool.query('SELECT NOW() AS now');
    return res.json({ ok: true, connectedAt: result.rows[0]?.now });
  } catch (error) {
    console.error('Database health check failed:', error);
    return res.status(500).json({ ok: false, error: 'Database connection failed.' });
  }
});

app.post('/api/inquiries', async (req, res) => {
  try {
    const { name, phone, course, message } = req.body as {
      name?: string;
      phone?: string;
      course?: string;
      message?: string;
    };

    if (!name || !phone || !course) {
      return res.status(400).json({ ok: false, error: 'Name, phone, and course are required.' });
    }

    if (pool) {
      // Database is available, save to database
      await pool.query(
        'INSERT INTO inquiries (name, phone, course, message) VALUES ($1, $2, $3, $4)',
        [name, phone, course, message ?? '']
      );
    } else {
      // Development mode without database - log inquiry to console
      console.log('📝 New Inquiry (Development Mode - No DB):', {
        name,
        phone,
        course,
        message,
        timestamp: new Date().toISOString(),
      });
    }

    return res.status(201).json({ ok: true, message: 'Inquiry saved successfully.' });
  } catch (error) {
    console.error('Failed to save inquiry:', error);
    return res.status(500).json({ ok: false, error: 'Could not save inquiry right now.' });
  }
});

